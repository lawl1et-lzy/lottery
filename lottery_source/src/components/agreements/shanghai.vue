      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在接受本协议之前，请您仔细阅读本协议的全部内容，如果您不同意本协议的任何内容，请不要进行后续操作。代销者通过网络页面点击确认或以其它方式选择接受本协议，即表示代销者与北京中维达成协议并同意接受本协议的全部约定内容。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北京中维已与上海市福利彩票发行中心（以下简称“福彩中心”）及上海市体育彩票管理中心（以下简称“体彩中心”）建立合作关系（体彩中心及福彩中心合称彩票中心），并接受彩票中心的相关委托，代其发展彩票销售点，开拓相关彩票销售市场</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北京中维与代销者双方基于本合同成立彩票代销合作关系，而非劳动关系或劳务关系，且代销者对此已明确知悉。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就北京中维委托代理销售中国福利彩票或中国体育彩票（以下简称“福利彩票”、“体育彩票”）事宜签订本协议，具体内容约定如下。</p>
      <h4>第一条 委托事项</h4>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北京中维委托代销者在代销者所在地区设立彩票代销站点，销售彩票。相关彩票销售类型，由北京中维依据代销者实际状况，进行决定。</p>
      <h4>第二条 委托期限</h4>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 北京中维委托代销者代销彩票的期限为体彩代销自本协议接受之日起至2020年12月31日，福彩代销自本协议接受之日起至2021年4月30日止。协议生效之日起, 北京中维与代销者双方按本协议执行。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 协议期限内，北京中维对代销者每年12月初实行年审。如代销者未通过年审导致关闭代销站点的情况下，北京中维有权解除本协议，不受协议约定期限之约束。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 协议期满后，双方若希望继续建立彩票代销合作关系的，应另行订立相关协议。</p>
      <h4>第三条 代销费用及结算方式、时间</h4>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;代销站点应按规定销售电脑型彩票, 北京中维与代销者双方约定代销者按以下标准获取销售佣金: </p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 代销者按照电脑型彩票销售额的【6】%+【1】%公益分获取电脑型彩票销售佣金。其中6%的销售佣金由上海渠道方（上海悦溢维合商业管理有限责任公司）打款到代销者的银行账户，结算周期为月结，每月11号结算上一个自然月的销售佣金。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 代销者按照即开型彩票销售额的【6】%+【1】%公益分获取即开型彩票的销售佣金。其中6%的销售佣金直接在下一个订单货款中扣除。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1%公益分由北京中维按照销售流水即时返还到代销者在“北京中维”公众号/APP上注册的账户</p>
      <p style="font-size:12px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;[公益分说明:公益分可在”北京中维”公众号/APP的公益商城兑换商品]</p>
      <h4>第四条 北京中维的权利</h4>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 北京中维有权要求代销者遵守《彩票管理条例》、《彩票管理条例实施细则》等相关法规、规章和代销者所在地人民政府财政部门、民政部门、国务院民政部门设立的彩票发行机构方制定的有关彩票管理政策和规定、规范。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 北京中维接受彩票中心的授权，有权根据有关合同约定、管理规定及委托事项对代销者销售彩票的行为进行监督、检查、管理；北京中维对代销者设立的代销站点的日常管理，由北京中维及北京中维委托的区县地推团队共同实施。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 北京中维有权根据国家政策调整，调整彩票代销费比例 ，并及时书面告知代销者。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. 因国家或地方政策调整或市政规划要求，代销者需变更站点地址或无法正常销售的，变更调整期间北京中维有权暂停 履行本协议。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. 因彩票业务需要，北京中维将无偿为代销者的彩票销售网点安装终端播放系统（显示屏），播放彩票信息及其他合规合法广告信息，北京中维无偿提供硬件及统一的播放内容。协议终止后，北京中维有权取回显示屏。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6. 代销者应遵守烟草专卖专营的相关管理规定，如若违反烟草专卖专营的相关管理规定，北京中维有权单方面解除本协议，且不承担相关违约责任。</p>
      <h4>第五条 北京中维的义务</h4>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 北京中维应在彩票代销站点验收合格且与代销者签订彩票代销协议后，协调彩票中心在15个工作日内向代销者发放相关彩票代销证，办理好代销证之后的15个工作日内提供合格的彩票销售设备供代销者使用。代销者所使用的彩票销售设备由北京中维提供，代销者不享有其所有权。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 北京中维接到代销者彩票销售设备发生故障的报修申请后，24小时内作出响应，并予以维修。经维修后仍不能正常使用的，应在48小时内进行更换。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 北京中维应对代销者及代销者聘用彩票销售员进行上岗前的培训。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. 北京中维应在接到代销者提前终止合同或变更彩票销售设备的申请后15个工作日内给予答复。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. 北京中维有义务向代销者提供票面完整、有效的彩票。</p>
