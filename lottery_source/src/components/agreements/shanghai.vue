      <h4>第七条 代销者的义务</h4>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. 代销者应当遵守《彩票管理条例》、《彩票管理条例实施细则》等相关规定、规章和当地人民政府财政部门、民政部门、国务院民政部门设立的彩票发行机构及彩票中心制定的有关彩票管理政策和规定、规范。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 代销者应具有完全民事行为能力，符合北京中维要求的主体资格，具备北京中维要求的固定经营场所、彩票销售条件。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 代销者须根据北京中维要求申请和办理用于电脑型彩票销售设备的有线或无线通信专用线路(费用自理)。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. 代销者应按彩票中心规定的销售时间开展经营活动，彩票休市或北京中维通知调整销售时间除外。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. 代销者应在彩票销售场所显著位置悬挂彩票代销证，不得转借、出租、出售；张贴“不得向未成年人销售彩票”警示性标语。</p>
