      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. 因电力、通讯等非双方的原因造成不能正常销售福利彩票的，双方互不承担责任；</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. 由于非北京中维原因造成代销者及第三方人身伤害或者财产损失的，北京中维不承担责任；</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. 因国家法律、法规、政策调整导致本协议不能继续履行或者解除的，双方互不承担违约责任；</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. 凡因本协议引起的或与本协议有关的任何争议，均应提交上海仲裁委员会按照该会仲裁规则进行仲裁。仲裁裁决是终局的，对双方当事人均有约束力。</p>
