      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;9. 代销者应当规范使用、维护北京中维提供的彩票销售设备，不得转借、出租、出售北京中维提供的彩票销售设备，不得擅自改变北京中维提供的彩票销售设备用途，不得拆卸北京中维提供的彩票销售设备或更换其零部件；不得查阅、修改、复制、删除北京中维提供的彩票销售设备装载的程序和有关数据文件，或者安装、运行其他程序和文件。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;代销者在使用彩票销售设备及本协议项下其他各项设备过程中，应保障相关设备安全，并应保证在双方代销合作关系终止时，交还完好的设备器材。若因非设备本身质量原因以外，导致设备毁损、灭失的，代销者应承担设备修复或设备重置所产生的一切费用。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;10. 代销者不得向未成年人销售彩票和兑付奖金。</p>
      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11. 代销者如果提前解除彩票代销合同，应当向北京中维送交书面申请，经北京中维书面同意后方能停止销售彩票，并按规定办理有关手续，结清相关款项。彩票代销合同权利义务终止后20个工作日内，由地推团队回收代销证等物料。</p>
